import { mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";

import {
  buildApiSchemaGrantSql,
  buildApiViewName,
  buildPostgresBootstrapSql,
  buildTransactionLocalSettingsSql
} from "../../framework/core/db-drizzle/src/index.ts";

const rootDir = process.cwd();
const outputDir = path.join(rootDir, "ops", "postgres");

const isolatedPluginIds = [
  ...listPackageDirectoryNames(path.join(rootDir, "plugins", "connectors")),
  ...listPackageDirectoryNames(path.join(rootDir, "plugins", "migrations"))
].sort((left, right) => left.localeCompare(right));

const apiProjections = [
  {
    viewName: buildApiViewName("crm", "contacts", "summary"),
    sourceSchema: "crm",
    sourceTable: "contacts",
    columns: ["id", "tenant_id", "email", "status"],
    where: "status <> 'inactive'"
  },
  {
    viewName: buildApiViewName("sales", "opportunities", "summary"),
    sourceSchema: "sales",
    sourceTable: "opportunities",
    columns: ["id", "tenant_id", "stage", "amount_cents", "currency"],
    where: "stage <> 'closed-lost'"
  },
  {
    viewName: buildApiViewName("marketing", "campaigns", "summary"),
    sourceSchema: "marketing",
    sourceTable: "campaigns",
    columns: ["id", "tenant_id", "segment_key", "status", "launch_at"]
  },
  {
    viewName: buildApiViewName("booking", "reservations", "summary"),
    sourceSchema: "booking",
    sourceTable: "reservations",
    columns: ["id", "tenant_id", "resource_class", "resource_id", "slot_start", "slot_end", "confirmation_status"]
  }
];

const bootstrapSql = buildPostgresBootstrapSql({
  pluginIds: isolatedPluginIds,
  tenantTables: [
    { schema: "identity", table: "users", forceRls: true },
    { schema: "crm", table: "contacts", forceRls: true },
    { schema: "sales", table: "opportunities", forceRls: true },
    { schema: "marketing", table: "campaigns", forceRls: true },
    { schema: "booking", table: "reservations", forceRls: true }
  ],
  apiProjections
});

const apiGrantSql = isolatedPluginIds.flatMap((pluginId) =>
  buildApiSchemaGrantSql(
    pluginId,
    apiProjections.map((projection) => projection.viewName)
  )
);

const transactionContextSql = buildTransactionLocalSettingsSql({
  tenantId: "tenant_example",
  actorId: "user_example",
  requestId: "req_example"
});

mkdirSync(outputDir, { recursive: true });

writeFileSync(path.join(outputDir, "platform-bootstrap.sql"), `${bootstrapSql.join("\n")}\n${apiGrantSql.join("\n")}\n`, "utf8");
writeFileSync(path.join(outputDir, "transaction-context.sql"), `${transactionContextSql.join("\n")}\n`, "utf8");
writeFileSync(
  path.join(outputDir, "compose.yaml"),
  `services:
  postgres:
    image: postgres:16-alpine
    container_name: platform-postgres
    environment:
      POSTGRES_DB: platform
      POSTGRES_USER: platform_admin
      POSTGRES_PASSWORD: platform_admin
    ports:
      - "5432:5432"
    volumes:
      - postgres-data:/var/lib/postgresql/data
      - ./platform-bootstrap.sql:/docker-entrypoint-initdb.d/10-platform-bootstrap.sql:ro
volumes:
  postgres-data:
`,
  "utf8"
);
writeFileSync(
  path.join(outputDir, "README.md"),
  `# Postgres Bootstrap Assets

Generated by \`bun run db:render:postgres\`.

Contents:

- \`platform-bootstrap.sql\`: role, schema, RLS, and curated \`api\` projection bootstrap SQL
- \`transaction-context.sql\`: transaction-local request context statements for tenant/actor/request metadata
- \`compose.yaml\`: local Postgres bootstrap for development and security review

Recommended local flow:

1. \`export PATH="$HOME/.bun/bin:$PATH"\`
2. \`bun run db:render:postgres\`
3. \`docker compose -f ops/postgres/compose.yaml up -d\`
4. Apply domain migrations with the migrator role after reviewing the generated SQL.

Notes:

- The bootstrap SQL assumes canonical platform schemas such as \`crm\`, \`sales\`, \`marketing\`, and \`booking\`.
- Curated \`api\` views are intended for isolated plugins and sidecar services; same-process trusted plugins should still go through host services and repositories.
- Runtime roles remain non-owning and do not receive \`BYPASSRLS\`.
`,
  "utf8"
);

console.log(`Rendered Postgres assets to ${path.relative(rootDir, outputDir)}`);

function listPackageDirectoryNames(directoryPath) {
  try {
    return readdirSync(directoryPath).filter((entry) => statSync(path.join(directoryPath, entry)).isDirectory());
  } catch {
    return [];
  }
}
